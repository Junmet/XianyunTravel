<template>
  <div class="flight-item">
    <div @click="isShow = !isShow">
      <!-- 显示的机票信息 -->
      <el-row type="flex" align="middle" class="flight-info">
        <el-col :span="6">
          <span>{{data.airline_name}}</span>
          {{data.flight_no}}
        </el-col>
        <el-col :span="12">
          <el-row type="flex" justify="space-between" class="flight-info-center">
            <el-col :span="8" class="flight-airport">
              <strong>{{data.dep_time}}</strong>
              <span>{{data.org_airport_name}}{{data.org_airport_quay}}</span>
            </el-col>
            <el-col :span="8" class="flight-time">
              <!-- 时间差 -->
              <span>{{mistiming}}</span>
            </el-col>
            <el-col :span="8" class="flight-airport">
              <strong>{{data.arr_time}}</strong>
              <span>{{data.dst_airport_name}}{{data.dst_airport_quay}}</span>
            </el-col>
          </el-row>
        </el-col>
        <el-col :span="6" class="flight-info-right">
          ￥
          <span class="sell-price">{{data.base_price/2}}</span>起
        </el-col>
      </el-row>
    </div>
    <div class="flight-recommend" v-if="isShow">
      <!-- 隐藏的座位信息列表 -->
      <el-row
        type="flex"
        justify="space-between"
        align="middle"
        v-for="(item,index) in data.seat_infos"
        :key="index"
      >
        <el-col :span="4">低价推荐</el-col>
        <el-col :span="20">
          <el-row type="flex" justify="space-between" align="middle" class="flight-sell">
            <el-col :span="16" class="flight-sell-left">
              <span>{{item.name}}</span>
              | {{item.supplierName}}
            </el-col>
            <el-col :span="5" class="price">￥{{item.par_price}}</el-col>
            <el-col :span="3" class="choose-button">
              <el-button type="warning" size="mini">选定</el-button>
              <p>剩余：{{item.discount}}</p>
            </el-col>
          </el-row>
        </el-col>
      </el-row>
    </div>
  </div>
</template>

<script>
export default {
  data() {
    return {
      isShow: false
    };
  },
  // props：有一种是数组的方式  一种是对象的方式（适合团队合作，知道传过来的数据是什么数据类型）
  props: {
    // 数据
    data: {
      // 数据类型是一个对象
      type: Object,
      // 要以函数的形式传一个对象
      default() {
        return {};
      }
    }
  },
  // 计算属性  实现时间差
  computed: {
    mistiming() {
      // 切割字符串 返回一个数组
      let arrtime = this.data.arr_time.split(":");
      let deptime = this.data.dep_time.split(":");

      // 1小时为60分钟
      let arr = arrtime[0] * 60 + +arrtime[1];
      let dep = deptime[0] * 60 + +deptime[1];

      //如果是第二天需要加24小时 要不然会是个负数
      if ((arr - dep) < 0) {
        arr += 24 * 60;
      }
      let res = arr - dep;
      return `${Math.floor(res / 60)}时${res % 60}分`;
    }
  }
};
</script>

